package hima.aarieats;

public class Vendors {

    String VendorId;
    String VendorName;
    String UserName;
    String email;
    String VendorAddress;
    String VendorCity;
    String VendorState;
    String VendorPhno;
    String VendorLat;
    String VendorLng;

    public String getVendorId() {
        return VendorId;
    }

    public void setVendorId(String vendorId) {
        VendorId = vendorId;
    }

    public String getVendorName() {
        return VendorName;
    }

    public void setVendorName(String vendorName) {
        VendorName = vendorName;
    }

    public String getUserName() {
        return UserName;
    }

    public void setUserName(String userName) {
        UserName = userName;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getVendorAddress() {
        return VendorAddress;
    }

    public void setVendorAddress(String vendorAddress) {
        VendorAddress = vendorAddress;
    }

    public String getVendorCity() {
        return VendorCity;
    }

    public void setVendorCity(String vendorCity) {
        VendorCity = vendorCity;
    }

    public String getVendorState() {
        return VendorState;
    }

    public void setVendorState(String vendorState) {
        VendorState = vendorState;
    }

    public String getVendorPhno() {
        return VendorPhno;
    }

    public void setVendorPhno(String vendorPhno) {
        VendorPhno = vendorPhno;
    }

    public String getVendorLat() {
        return VendorLat;
    }

    public void setVendorLat(String vendorLat) {
        VendorLat = vendorLat;
    }

    public String getVendorLng() {
        return VendorLng;
    }

    public void setVendorLng(String vendorLng) {
        VendorLng = vendorLng;
    }

}
